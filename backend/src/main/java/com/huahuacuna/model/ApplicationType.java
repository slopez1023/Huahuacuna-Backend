package com.huahuacuna.model;

/**
 * Enumeración que define los tipos de solicitud de participación
 * disponibles en la fundación.
 *
 * @author Fundación Huahuacuna
 * @version 1.0
 */
public enum ApplicationType {
    /**
     * Solicitud para ser voluntario de la fundación
     */
    VOLUNTARIO,

    /**
     * Solicitud para ser padrino/madrina de un niño
     */
    PADRINO
}